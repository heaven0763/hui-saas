package com.chuangbang.plugins.baidu.yun.map.model;

public class Coordinate {
	//纬度值
	private float lat;
	//经度值
	private float lng;
	
	public float getLat() {
		return lat;
	}
	public void setLat(float lat) {
		this.lat = lat;
	}
	public float getLng() {
		return lng;
	}
	public void setLng(float lng) {
		this.lng = lng;
	}


	
}
