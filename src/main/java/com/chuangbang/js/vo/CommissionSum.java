package com.chuangbang.js.vo;

public class CommissionSum {
	private String hotelName;
	private Double totalCommissionAmount;
	private Double totalOrderMoney;
	private String totalCommissionOrder;
	
	
	public String getHotelName() {
		return hotelName;
	}
	public void setHotelName(String hotelName) {
		this.hotelName = hotelName;
	}
	
	public Double getTotalOrderMoney() {
		return totalOrderMoney;
	}
	public void setTotalOrderMoney(Double totalOrderMoney) {
		this.totalOrderMoney = totalOrderMoney;
	}
	public String getTotalCommissionOrder() {
		return totalCommissionOrder;
	}
	public void setTotalCommissionOrder(String totalCommissionOrder) {
		this.totalCommissionOrder = totalCommissionOrder;
	}
	public Double getTotalCommissionAmount() {
		return totalCommissionAmount;
	}
	public void setTotalCommissionAmount(Double totalCommissionAmount) {
		this.totalCommissionAmount = totalCommissionAmount;
	}
	
	
}
